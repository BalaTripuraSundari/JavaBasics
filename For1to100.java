package com;

public class For1to100 {
	public static void main(String[]args ){
		int i=0;
		System.out.print("numbers in while loop:");
	while(i<=100){
		System.out.print(i + " ");
		i++;
		
	}
	i=0;
	System.out.println("numbers in do-while loop:");
	do {
    System.out.println(i + " ");
    i++;
	}
	while(i<=100);
	
	}
}
